// Get exercise info with fallback for unknown exercises
export function getExerciseInfo(exerciseName: string): ExerciseInfo | null {
    // Try exact match
    if (EXERCISE_DATABASE[exerciseName]) {
        return EXERCISE_DATABASE[exerciseName];
    }

    // Try case-insensitive match
    const lowerName = exerciseName.toLowerCase();
    for (const [name, info] of Object.entries(EXERCISE_DATABASE)) {
        if (name.toLowerCase() === lowerName) {
            return info;
        }
    }

    // Try partial match
    for (const [name, info] of Object.entries(EXERCISE_DATABASE)) {
        if (lowerName.includes(name.toLowerCase()) || name.toLowerCase().includes(lowerName)) {
            return info;
        }
    }

    return null; // Unknown exercise
}
